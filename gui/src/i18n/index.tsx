export const I18nContext = React.createContext<I18nContextType | undefined>(undefined)

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = React.useState<Lang>(() => {
    const saved = (typeof window !== 'undefined' && localStorage.getItem(I18N_KEY)) as Lang | null
    return saved === 'en' || saved === 'zh' ? saved : 'zh'
  })

  const setLang = (next: Lang) => {
    setLangState(next)
    try { localStorage.setItem(I18N_KEY, next) } catch {}
  }

  const t = React.useCallback((key: string) => {
    const dict = resources[lang]
    return dict[key] || key
  }, [lang])

  const value = React.useMemo(() => ({ lang, setLang, t }), [lang, t])
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const ctx = React.useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
